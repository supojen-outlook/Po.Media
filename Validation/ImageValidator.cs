namespace Po.Media.Validation;

internal class ImageValidator : FileValidatorBase
{
    public ImageValidator() : base(
        typeName:"IMAGE FILE",
        extensions:[".jpeg", ".jpg", ".png"],
        magicNumbers:[
            [0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A],
            [0xFF, 0xD8, 0xFF, 0xE0],
            [0xFF, 0xD8, 0xFF, 0xE1],
            [0xFF, 0xD8, 0xFF, 0xE2],
            [0xFF, 0xD8, 0xFF, 0xE3],
        ]) { }
}